# O arquivo __init__.py em um diretório do Python serve para indicar que o 
# diretório deve ser tratado como um pacote Python. Vou explicar em mais detalhes:

# Marcação de Pacote:

# O arquivo __init__.py é usado para marcar um diretório como um pacote Python. 
# Quando o Python encontra um diretório contendo um arquivo __init__.py, 
# ele trata esse diretório como um pacote, permitindo a importação de módulos e subpacotes contidos nele.


# Inicialização de Pacote:

# O conteúdo do arquivo __init__.py é executado quando o pacote é importado. 
# Isso permite que você execute código de inicialização específico do pacote, 
# como a definição de variáveis globais, configuração de parâmetros ou 
# qualquer outra lógica de inicialização necessária.